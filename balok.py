def luas_balok(p, l, t):
    '''Luas = 2*(p*l+p*t+l*t)'''
    luas = 2*(p*l + p*t + l*t)
    print("Luas = ",luas)
    
def keliling_balok(p, l, t):
    '''Keliling = 4(p+l+t)'''
    keliling = 4*(p + l + t)
    print("Keliling = ", keliling)
    
def volume_balok(p, l, t):
    '''Volume=p*l*t''' 
    volume = p*l*t
    print("Volume = ",volume)
